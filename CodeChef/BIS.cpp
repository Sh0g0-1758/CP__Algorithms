int main()
{
    fastio();
    ll t;
    cin >> t;
    while (t--)
    {
        ll n;
        cin >> n;
        string s;
        cin >> s;
        ll n11=0,n10=0,n01=0,n00=0;
        for(ll i = 0; i < n;i+=2) {
            pair<ll,ll> p;
            p.first=s.at(i)-'0';
            p.second=s.at(i+1)-'0';
            if(p.first==0 and p.second==0) n00++;
            else if(p.first==0 and p.second==1) n01++;
            else if(p.first==1 and p.second==0) n10++;
            else if(p.first==1 and p.second==1) n11++;
        }
        ll ans = 2 * n00 + 2 * n11;
        if(n10>0) ans++,n10--;
        if(n10>0) ans++,n10--;
        if(n01>0) ans+=2,n01--;
        cout << ans << endl;
    }
    return 0;
}