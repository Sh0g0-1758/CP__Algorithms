int lengthOfLongestSubstring(string s) {
    int ans = 0;
    for(int i = 0; i < s.size();i++) {
        map<int,int> store;
        store[(int)s[i]]++;
        int cnt = 1;
        // debug(i)
        for(int j = i+1;j<s.size();j++) {
            // debug(store)
            if(store[(int)s[j]] == 0) {
                store[(int)s[j]]++;
                cnt++;
            } else {
                break;
            }
        }
        if(cnt > ans) {
            ans = cnt;
        }
    }
    return ans;
}

